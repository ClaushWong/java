    private void DisplayData(ArrayList<ArrayList<String>> data)
    {
        DefaultTableModel tmodel = (DefaultTableModel) Table.getModel();
        
        if (tmodel.getRowCount() > 0)
        {
            for (int i = tmodel.getRowCount() -1 ; i > -1; i--)
            {
                tmodel.removeRow(i);
            }
        }
        
        for (ArrayList<String> i : data)
        {
            tmodel.addRow(new Object[] 
            {
                i.get(0),
                i.get(1),
                i.get(2),
                i.get(3)
            });
        }
    }
    
    private void AddDataToTable(ArrayList<String> data)
    {
        DefaultTableModel tmodel = (DefaultTableModel) Table.getModel();
        tmodel.addRow(new Object[]{
            data.get(0),
            data.get(1),
            data.get(2),
            data.get(3)
        });
    }
    
    private JTable CreateTable()
    {
        model.addColumn("Donator Name");
        model.addColumn("Donator Gender");
        model.addColumn("Book Name");
        model.addColumn("Book Detail");
        
        return new JTable(model);
    }
    
    public void UpdateSelectedData(int row, ArrayList<String> data)
    {
        // Modify
        formatted_data.set(row, data);
        
        // Reset the table
        DisplayData(formatted_data);
    }
    
    private void DeleteSelectedData(int row)
    {
        formatted_data.remove(row);
        
        DisplayData(formatted_data);
    }
    
    private void ResetInput()
    {
        donatorName.setText("");
        donatorGender.setSelectedItem("male");
        bookName.setText("");
        bookDetail.setText("");
    }
    
    private void AddDonationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddDonationButtonActionPerformed
        String name = donatorName.getText();
        String gender = (String)donatorGender.getSelectedItem();
        String book_name = bookName.getText();
        String book_detail = bookDetail.getText();
        
        ArrayList<String> data_placeholder = new ArrayList();
        data_placeholder.add(name);
        data_placeholder.add(gender);
        data_placeholder.add(book_name);
        data_placeholder.add(book_detail);
        formatted_data.add(data_placeholder);
        
        AddDataToTable(data_placeholder);
        ResetInput();
    }//GEN-LAST:event_AddDonationButtonActionPerformed

    private void OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenActionPerformed
        try {
            file_path = f_m.GetFileAddress();
        }
        catch (InvalidFileException ex) {
            JOptionPane.showMessageDialog(this, ex);
            return;
        }
        
        String unformatted_data = "";
        try {
            unformatted_data = f_m.GetData(this, file_path);
            if (!(unformatted_data.equalsIgnoreCase("null")))
                JOptionPane.showMessageDialog(this,"Load Successfully");
        } catch (IOException ex) {
            Logger.getLogger(MainWindow3.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!(unformatted_data.equalsIgnoreCase("null")))
            formatted_data = dataFormatter.CSV_Derived(unformatted_data);
        
        DisplayData(formatted_data);
        ResetInput();
    }//GEN-LAST:event_OpenActionPerformed

    private void SaveAsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveAsActionPerformed
        try
        {
            file_path = f_m.GetFileAddress();
        }
        catch (InvalidFileException e)
        {
            JOptionPane.showMessageDialog(this, e);
            return;
        }
        
        try {
            f_m.Store_Data(file_path, formatted_data);
            if (!(file_path.equalsIgnoreCase("null")))
            {
                JOptionPane.showMessageDialog(this,"Saved Successfully");
            }
        } catch (IOException ex) {
            Logger.getLogger(MainWindow3.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SaveAsActionPerformed

